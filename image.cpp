void setPixel(int x, int y, unsigned char color) {
    if (color == 0) {
        image[x/8 + 25*y] = image[x/8 + 25*y] & (0xfe << (7-x%8)); //white
    }
    else if (color == 1) {
        image[x/8 + 25*y] = image[x/8 + 25*y] | (0x01 << (7-x%8)); //black
    }
    else if (color == 2) {
        image[x/8 + 25*y] = image[x/8 + 25*y] ^ (0x01 << (7-x%8)); //swap
    }
}

void drawRect(int x, int y, int width, int height, unsigned char color) {
    for (int x2 = 0; x2 < width; x2++) {
        for (int y2 = 0; y2 < height; y2++) {
            setPixel(x + x2, y + y2, color);
        }
    }
}

void initializeImage() {
    for (int i = 0; i < X_RES/8 * Y_RES; i++) {
        image[i] = 0;
    }
}

void drawCharacter(int x, int y, char c) {
   for (int i = 0; i < 13; i++){
       image[x/8 + 25*(y+12-i)] = image[x/8 + 25*(y+12-i)] | (letters[c - 32][i] >> (x%8));
       if (x%8 > 0) {
           image[x/8 + 25*(y+12-i) + 1] = image[x/8 + 25*(y+12-i) + 1] | (letters[c - 32][i] << (7-x%8));
       }
   } 
}

void drawString(int x, int y, std::string str) {
    for (int i = 0; i < str.length(); i++) {
        drawCharacter(x + 10*i, y, str[i]);
    }
}

